package ex1;

/**
 * @author gmayeul
 * Effectue des calculs
 */
public class Calcul {

	/**
	 * @param a
	 * @param b
	 * @return somme a + b
	 */
	public int additionner(int a, int b){
		return a + b;
	}
	
	/**
	 * @param a
	 * @param b
	 * @return résultat de a - b
	 */
	public int soustraire(int a, int b){
		return a - b;
	}
}
